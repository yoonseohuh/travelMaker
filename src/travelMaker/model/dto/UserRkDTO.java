package travelMaker.model.dto;

public class UserRkDTO {

	private Integer rkNo;
	private String rkName;
	private Integer rkNeed;
	private String rkExpl;
	private String rkAddExpl;
	public Integer getRkNo() {
		return rkNo;
	}
	public void setRkNo(Integer rkNo) {
		this.rkNo = rkNo;
	}
	public String getRkName() {
		return rkName;
	}
	public void setRkName(String rkName) {
		this.rkName = rkName;
	}
	public Integer getRkNeed() {
		return rkNeed;
	}
	public void setRkNeed(Integer rkNeed) {
		this.rkNeed = rkNeed;
	}
	public String getRkExpl() {
		return rkExpl;
	}
	public void setRkExpl(String rkExpl) {
		this.rkExpl = rkExpl;
	}
	public String getRkAddExpl() {
		return rkAddExpl;
	}
	public void setRkAddExpl(String rkAddExpl) {
		this.rkAddExpl = rkAddExpl;
	}
	
}
