package travelMaker.model.dto;

public class SmallPosDTO {

	private Integer posCate;
	private Integer posNo;
	private String posName;
	private String posExpl;
	private String posRoot;
	public Integer getPosCate() {
		return posCate;
	}
	public void setPosCate(Integer posCate) {
		this.posCate = posCate;
	}
	public Integer getPosNo() {
		return posNo;
	}
	public void setPosNo(Integer posNo) {
		this.posNo = posNo;
	}
	public String getPosName() {
		return posName;
	}
	public void setPosName(String posName) {
		this.posName = posName;
	}
	public String getPosExpl() {
		return posExpl;
	}
	public void setPosExpl(String posExpl) {
		this.posExpl = posExpl;
	}
	public String getPosRoot() {
		return posRoot;
	}
	public void setPosRoot(String posRoot) {
		this.posRoot = posRoot;
	}
	
	
}
